//比較する対象
let num1 = 20;
let num2 = 20;

//大きい ( > )
console.log( num1 > num2 );
//小さい ( < )
console.log( num1 < num2 );
//以上 ( >= )
console.log( num1 >= num2 );
//以下 ( <= )
console.log( num1 <= num2 );


//等しい・等しくない
//比較する対象
